#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*s1_c;

	s1_c = s1;
	while (*s1_c)
		s1_c++;
	while (*s2 && n--)
		*s1_c++ = *s2++;
	*s1_c = '\0';
	return (s1);
}
