#include "mtrx.h"

void	ft_mtrx_destroy(t_mtrx **mtrx)
{
	if (!mtrx || !*mtrx)
		return ;
	free((*mtrx)->mtrx);
	free(*mtrx);
	*mtrx = NULL;
}
